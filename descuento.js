function descuento() {

 let ingresarNumero = prompt("Ingrese un numero: ")
 let ingresarDescuento = prompt("Ingresar descuento: ")

 resultado = (ingresarNumero * (ingresarDescuento /100))

 alert("El precio final es de :" + ( ingresarNumero - resultado))

}

descuento();



